function isIsogram(str) {
    //convert the string to lowercase or uppercase 
    //add the elements into an array 


    let i
    let j
    str.toLowerCase()
    //sort the elements in the array  
    for (i < str.length; ++i;) {
        for (j = j + 1; j < str.length; ++j)
            if (str[i] == str[j])
                return false

        return true


    }
    //check if the first index is equal to the zero index


}